/**
 * The Adapter Pattern converts the interface of a class into another interface clients expect.
 * Adapter lets clases work together that couldn't otherwise because of incompatible interfaces.
 * <p>
 * Comparison with The Facade Pattern:
 * Adapter's intent is to alter the interface that it matches the one client is expecting.
 * <p>
 * Comparison with The Decorator Pattern:
 * An adapter wraps an object to change its interface, a decorator wraps an object to add new
 * behaviors and responsibilities.
 *
 * @author Radek Koubsky (radekkoubsky@gmail.com)
 */
