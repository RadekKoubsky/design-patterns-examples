/**
 * The Proxy Pattern provides a surrogate or placeholder for another object
 * to contol access to it.
 * <p>
 * Use The Proxy Pattern to create a representative object that controls
 * access to another object, which may be remote, expensive to create or
 * in need of securing.
 * </p>
 * <p>
 * Comparison wit other patterns:
 * </p>
 * <p>
 * Decorator: Wraps another object and provides additional behavior for it.
 * </p>
 * <p>
 * Facade: Wraps a bunch of objects to simplify their interface.
 * </p>
 * <p>
 * Adapter: Wraps another object to and provides a different interface to it.
 * </p>
 * <p>
 * Proxy: Wraps another object to control access to it.
 * </p>
 *
 * @author Radek Koubsky (radekkoubsky@gmail.com)
 */
